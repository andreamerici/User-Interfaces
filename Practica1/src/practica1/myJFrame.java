    /**
     * Method associated with the changes int the first field
     * @param evt: KeyEvent 
     */
    private void valueField1KeyReleased(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_valueField1KeyReleased
        
        // Reset the errorLabel
        errorLabel.setText("");
        
        String input = valueField1.getText();
        
        if(input.length() != 0){
            
            // The input is numeric
            if(isNumeric(input)){
                double valueEntered = Float.parseFloat(input);
                double valueConverted = conversion(valueEntered);
            
                // Format of the number with 3 digits
                DecimalFormat df = new DecimalFormat("###.###");
                String result = df.format(valueConverted);
                valueField2.setText(result);
            
            }else if (input.charAt(input.length()-1) == '.'){
                // If the last digit is '.', reset and wait for the next digit
                errorLabel.setText("");
                valueField2.setText("");
            
            }else{
                errorLabel.setText("The entered value is not correct");
                valueField2.setText("");
            }
        }

    }//GEN-LAST:event_valueField1KeyReleased

