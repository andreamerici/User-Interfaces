    /**
     * Method for the setting of the conversion options
     */
    public void setUnitMeasures(){
        if(CONVERSION_MI_TO_KM){
            factorLabel.setText("1 mi = 1.609 km");
            unitMeasureLabel1.setText("mi");
            unitMeasureLabel2.setText("km");   
        }else{
            factorLabel.setText("1 km = 0.621 mi");
            unitMeasureLabel1.setText("km");
            unitMeasureLabel2.setText("mi"); 
        }
        
        // Reset the field value
        valueField1.setText("");
        valueField2.setText("");
    }
        
    /**
     * Method for checking if one string is a numeric value
     * @param str
     * @return true if the string respects the Regular Expression
     */
    public static boolean isNumeric(final String str) {
        
        // In this Regular Expression 'd' is decimal
        return str.matches("\\d+(\\.\\d+)?");
    }

    /**
     * Method for the conversion of the value of measure
     * @param value: the value that has to be converted
     * @return the converted value
     */
    public double conversion(double value){
        
        double res;
        
        if(CONVERSION_MI_TO_KM){
            res = (value * 1.609);
        }else{
            res = (value / 1.609);
        }

        return res;
    }
    
